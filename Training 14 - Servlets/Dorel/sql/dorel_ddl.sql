CREATE TABLE PRODUCTS (
		PRODUCT_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
		PRODUCT_NAME VARCHAR(200) UNIQUE	
	);

CREATE TABLE USERS (
		USER_ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
		USER_NAME VARCHAR(200),	
		USER_PASS VARCHAR(200)
	);

