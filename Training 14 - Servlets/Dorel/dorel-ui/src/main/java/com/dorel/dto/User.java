package com.dorel.dto;

/**
 * The User.java.
 * @author dungureanu
 */
public class User {

	/** The id. */
	private int id;

	/** The user. */
	private String user;

	/** The pass. */
	private String pass;

	/**
	 * Gets the id.
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * Sets the id.
	 * @param id the new id
	 */
	public void setId(final int id) {
		this.id = id;
	}

	/**
	 * Gets the user.
	 * @return the user
	 */
	public String getUser() {
		return user;
	}

	/**
	 * Sets the user.
	 * @param user the new user
	 */
	public void setUser(final String user) {
		this.user = user;
	}

	/**
	 * Gets the pass.
	 * @return the pass
	 */
	public String getPass() {
		return pass;
	}

	/**
	 * Sets the pass.
	 * @param pass the new pass
	 */
	public void setPass(final String pass) {
		this.pass = pass;
	}

}
